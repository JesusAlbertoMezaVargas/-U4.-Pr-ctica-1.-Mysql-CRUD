    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       //NOTA:dbes de validar que los cacmpos de texto tengan data
        Persona persona = new Persona(0,
                                        jTextField1.getText(),
                                        jTextField2.getText(),
                                        jTextField3.getText());
        boolean respuesta = base.insertar(persona);
        if(respuesta){
            setTitle("se inserto: ");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            cargarTabla();
        }else{
           JOptionPane.showMessageDialog(this,"ERRO: ALGO PASO..."); 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
     renglonSeleccionado = jTable1.getSelectedRow();
     if(evt.getClickCount()>=2){
         Persona p = new Persona(
               Integer.parseInt(m.getValueAt(renglonSeleccionado,0).toString()),
                  m.getValueAt(renglonSeleccionado,1).toString(),
                m.getValueAt(renglonSeleccionado,2).toString(),
                m.getValueAt(renglonSeleccionado,3).toString());
    
     Actualizar dialogo = new Actualizar(this, true, p);
  
      dialogo.setVisible(true);
          
     }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       if(renglonSeleccionado == -1){
           JOptionPane.showMessageDialog(this, "ERROR: selecciona renglon para borrar");
           return;
       }
       
       int respuesta = JOptionPane.showConfirmDialog(this, 
               "Seguro que deseas eliminar");
       
       if(respuesta==JOptionPane.OK_OPTION){
          String idEliminar = m.getValueAt(renglonSeleccionado,0).toString();
       if(base.eliminar(idEliminar)){
           setTitle("SE ELIMINO CORRECTAMENTE");
           cargarTabla();
        }else{
           JOptionPane.showMessageDialog(this, "ERROR!!: NO SE ELIMINO!!");
    } 
}
       
       
    }//GEN-LAST:event_jButton2ActionPerformed


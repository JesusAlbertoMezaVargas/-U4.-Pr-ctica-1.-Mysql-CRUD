    private void cargarTabla(){
        ArrayList<Persona> resultados = base.mostrarTodos();
        
        int tamModelo = m.getRowCount();
        
        for(int i = 1;i<=tamModelo;i++){
            m.removeRow(0);
        }
        
        resultados.forEach(
        (Persona)-> m.addRow(Persona.toRenglon())
        );
        jTable1.setModel(m);
    }

